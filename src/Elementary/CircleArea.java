public class CircleArea {
    public static void main(String args[]) {
        double pi, r, a;
        r = 10.8; // радиус окружности
        pi = 3.1416; //pi, приблизительное значение
        a = pi * r * r; // вычисление площади
        System.out.println("Площадь круга составляет " + a);
    }
}
